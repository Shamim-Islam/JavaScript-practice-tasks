// findout Min number

const min = Math.min(45, 21, 65, 99, 126, 2, 5, -99);
console.log(min); // Output -99

// Find out Max Number

const max = Math.max(45, 21, 65, 99, 126, 2, 5, -99);
console.log(max); //Output- 126

// PI - 3.14

console.log(Math.PI); // Output- 3.141592653589793

// abs
console.log(Math.abs(5 - 10)); // Output- 5

// Round
console.log(Math.round(4.4)); // Output- 4

// floor
console.log(Math.floor(3.1)); // Output- 3

// ceil
console.log(Math.ceil(3.99)); // Output- 4

// random
console.log(Math.random() * 10); // Random number between 1-10

const rand = Math.round(Math.random() * 10); // Random number between 1-10 without decimel
console.log(rand);

// power

console.log(Math.pow(2, 3)); // Output- 8

console.log(2 ** 3); // Output- 8
